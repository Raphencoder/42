int		ft_sqrt(int nb)
{
	int	test;

	test = 1;
	if (nb == 4)
		return (2);
	while (test < (nb / 2))
	{
		if ((test * test) == nb)
			return (test);
		else
			test++;
	}
	return (0);
}
