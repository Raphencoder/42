#include <string.h>

char	*ft_strncat(char *s1, char *s2, size_t n)
{
	int	i;
	int	j;
	
	i = 0;
	while(s1[i] != '\0')
		i++;
	j = 0;
	while(s2[j] != '\0' && n > 0)
	{
			s1[i++] = s2[j++];
			n--;
	}
	if(s2[j] == '\0')
	{
			s1[i] ='\0';
			return (s1);
	}
	return (s1);
}
